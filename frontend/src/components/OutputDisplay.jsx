// ---- Agent Trace View ----
function TraceView({ trace }) {
  if (!trace?.length) return <p className="text-sm text-white/30">No trace data available.</p>

  return (
    <div className="space-y-2">
      <Card title="Agent execution trace">
        {trace.map((t, i) => (
          <div key={i} className={`flex items-center justify-between ${i > 0 ? 'mt-2 pt-2 border-t border-white/[0.04]' : ''}`}>
            <div className="flex items-center gap-2">
              {t.status === 'success' ? (
                <CheckCircle2 size={14} className="text-emerald-400" />
              ) : (
                <AlertTriangle size={14} className="text-red-400" />
              )}
              <span className="text-sm text-white/60 capitalize">{t.agent}</span>
            </div>
            <span className="text-xs text-white/25">{t.duration_seconds}s</span>
          </div>
        ))}
      </Card>
    </div>
  )
}

// ---- Shared Components ----
function Card({ title, badge, children }) {
  return (
    <div className="p-4 rounded-xl bg-white/[0.02] border border-white/[0.05]">
      {(title || badge) && (
        <div className="flex items-center justify-between mb-3">
          {title && <h4 className="text-xs font-medium text-white/30 uppercase tracking-wider">{title}</h4>}
          {badge && <span className="px-2 py-0.5 rounded text-[10px] text-white/25 bg-white/[0.03]">{badge}</span>}
        </div>
      )}
      {children}
    </div>
  )
}

function Label({ children, icon }) {
  return (
    <div className="flex items-center gap-1 mb-1.5">
      {icon && <span className="text-white/20">{icon}</span>}
      <span className="text-[10px] text-white/25 uppercase tracking-wider font-medium">{children}</span>
    </div>
  )
}

function RawView({ text }) {
  return (
    <Card title="Raw response">
      <pre className="text-sm text-white/50 whitespace-pre-wrap font-mono leading-relaxed overflow-auto max-h-[600px]">
        {text}
      </pre>
    </Card>
  )
}

function ErrorView({ message }) {
  return (
    <div className="p-4 rounded-xl bg-red-500/5 border border-red-500/15">
      <p className="text-sm text-red-400/70">{message}</p>
    </div>
  )
}
